function normalizeMediaItem(item) {
  return {
    ...item,
    author: item.creator,
    thumbnail: item.thumbnailUrl,
    permalink: item.pageUrl,
    sourceHost: (() => {
      try {
        return new URL(item.mediaUrl).hostname;
      } catch {
        return null;
      }
    })(),
    videoUrl: item.type === 'video' ? item.mediaUrl : null,
    videoDurationSec: item.type === 'video' ? item.duration : null,
    score: item.popularity,
    numComments: 0,
    subreddit: item.source === 'library' ? item.category : null,
    canPlayFullAudioInApp: item.type !== 'video' || true,
    videoHasAudio: item.type === 'video' ? true : null,
    videoHlsUrl: null,
    videoDashUrl: null,
    videoSourceKind: item.type === 'video' ? 'url' : null,
    videoIsPreviewSource: false
  };
}

function matchesSearch(item, search) {
  if (!search) return true;
  const needle = search.toLowerCase();
  return [item.title, item.creator, ...(item.tags || []), item.category]
    .filter(Boolean)
    .some((value) => String(value).toLowerCase().includes(needle));
}

function filterItems(items, filters) {
  return items.filter((item) => {
    if (filters.search && !matchesSearch(item, filters.search)) return false;
    if (filters.creator && item.creator !== filters.creator) return false;
    if (filters.tag && !(item.tags || []).includes(filters.tag)) return false;
    if (filters.type && filters.type !== 'all' && item.type !== filters.type) return false;
    return true;
  });
}

function sortItems(items, sort) {
  const copy = [...items];
  if (sort === 'popular') {
    return copy.sort((a, b) => (b.popularity || 0) - (a.popularity || 0));
  }
  return copy.sort((a, b) => (b.createdUtc || 0) - (a.createdUtc || 0));
}

function paginateItems(items, after, limit) {
  const offset = Number.parseInt(after || '0', 10) || 0;
  const slice = items.slice(offset, offset + limit);
  const nextAfter = offset + limit < items.length ? String(offset + limit) : null;
  return { items: slice, after: nextAfter, total: items.length };
}

const CACHE_TTL_MS = 5 * 60 * 1000;
const queryCache = new Map();

function getCached(key) {
  const cached = queryCache.get(key);
  if (!cached) return null;
  if (cached.expiresAt <= Date.now()) {
    queryCache.delete(key);
    return null;
  }
  return cached.value;
}

function setCached(key, value) {
  queryCache.set(key, { value, expiresAt: Date.now() + CACHE_TTL_MS });
}

export function queryMediaCatalog({ search = '', creator = '', tag = '', type = 'all', sort = 'newest', after = null, limit = 24 } = {}) {
  const key = JSON.stringify({ search, creator, tag, type, sort, after, limit });
  const cached = getCached(key);
  if (cached) return cached;

  const filtered = filterItems(MEDIA_CATALOG_SEED, { search, creator, tag, type });
  const sorted = sortItems(filtered, sort);
  const paginated = paginateItems(sorted.map(normalizeMediaItem), after, limit);
  setCached(key, paginated);
  return paginated;
}

export function listMediaCreators() {
  const creators = new Map();
  for (const item of MEDIA_CATALOG_SEED) {
    const current = creators.get(item.creator) || { name: item.creator, count: 0 };
    current.count += 1;
    creators.set(item.creator, current);
  }
  return Array.from(creators.values()).sort((a, b) => b.count - a.count || a.name.localeCompare(b.name));
}

export function listMediaTags() {
  const tags = new Map();
  for (const item of MEDIA_CATALOG_SEED) {
    for (const tag of item.tags || []) {
      const current = tags.get(tag) || { name: tag, count: 0 };
      current.count += 1;
      tags.set(tag, current);
    }
  }
  return Array.from(tags.values()).sort((a, b) => b.count - a.count || a.name.localeCompare(b.name));
}

export function listMediaFeed(limit = 24) {
  return queryMediaCatalog({ limit });
}
